    @Test
    fun generatedBuilderHasTypedProperties() {
        val result = compile()
        assertEquals(KotlinCompilation.ExitCode.OK, result.exitCode, "Compilation failed:\n${result.messages}")

        // OrganizationBuilder should have a 'name' property (Kotlin var compiles to getter+setter)
        val orgBuilderClass = result.classLoader.loadClass("$outputPackage.OrganizationBuilder")
        assertNotNull(orgBuilderClass, "OrganizationBuilder class not found")
        val orgNameGetter = orgBuilderClass.methods.firstOrNull { it.name == "getName" }
        val orgNameSetter = orgBuilderClass.methods.firstOrNull { it.name == "setName" }
        assertNotNull(orgNameGetter, "OrganizationBuilder should have getName() method")
        assertNotNull(orgNameSetter, "OrganizationBuilder should have setName() method")

        // AppUserBuilder should have 'name' and 'email' properties
        val appUserBuilderClass = result.classLoader.loadClass("$outputPackage.AppUserBuilder")
        assertNotNull(appUserBuilderClass, "AppUserBuilder class not found")
        val userNameGetter = appUserBuilderClass.methods.firstOrNull { it.name == "getName" }
        val emailGetter = appUserBuilderClass.methods.firstOrNull { it.name == "getEmail" }
        assertNotNull(userNameGetter, "AppUserBuilder should have getName() method")
        assertNotNull(emailGetter, "AppUserBuilder should have getEmail() method")
    }

