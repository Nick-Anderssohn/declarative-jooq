    // -----------------------------------------------------------------------
    // Test 6: Self-referential FK inserts with correct parent_id chain
    // -----------------------------------------------------------------------

    @Test
    fun selfReferentialFkInserts() {
        val result = compile(listOf(edgeCaseHarnessSource()))
        assertEquals(KotlinCompilation.ExitCode.OK, result.exitCode, "Compilation failed:\n${result.messages}")

        val ctx = freshContext()
        val harnessClass = result.classLoader.loadClass("$outputPackage.EdgeCaseHarness")
        val harnessInstance = harnessClass.getField("INSTANCE").get(null)
        val runMethod = harnessClass.getMethod("runSelfRef", DSLContext::class.java)
        runMethod.invoke(harnessInstance, ctx)

        // Verify 3 category rows were inserted
        val count = ctx.selectCount().from(DSL.table("category")).fetchOne(0, Int::class.java)!!
        assertEquals(3, count, "Expected 3 category rows")

        // Query all categories with their parent_id values
        val rows = ctx.select(DSL.field("name"), DSL.field("parent_id"))
            .from(DSL.table("category"))
            .fetch()

        val byName = rows.associateBy(
            { it.get(DSL.field("name")) as String },
            { it.get(DSL.field("parent_id")) }
        )

        assertNull(byName["Electronics"], "Electronics should have NULL parent_id")
        assertNotNull(byName["Phones"], "Phones should have non-null parent_id")
        assertNotNull(byName["Smartphones"], "Smartphones should have non-null parent_id")

        // Verify the chain: Phones.parent_id = Electronics.id, Smartphones.parent_id = Phones.id
        val electronicsId = ctx.select(DSL.field("id"))
            .from(DSL.table("category"))
            .where(DSL.field("name").eq("Electronics"))
            .fetchOne()?.get(DSL.field("id"))
        val phonesId = ctx.select(DSL.field("id"))
            .from(DSL.table("category"))
            .where(DSL.field("name").eq("Phones"))
            .fetchOne()?.get(DSL.field("id"))

        assertEquals(electronicsId, byName["Phones"], "Phones.parent_id should equal Electronics.id")
        assertEquals(phonesId, byName["Smartphones"], "Smartphones.parent_id should equal Phones.id")
    }

