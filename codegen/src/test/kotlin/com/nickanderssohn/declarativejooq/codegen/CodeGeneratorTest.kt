    /**
     * Creates an H2 in-memory DSLContext with the organization + app_user schema.
     * Uses a per-call unique DB name to keep each test isolated.
     */
    private fun freshContext(): DSLContext {
        // Use a unique DB name per call to avoid cross-test contamination
        val dbName = "test_${System.nanoTime()}"
        val url = "jdbc:h2:mem:$dbName;MODE=PostgreSQL;DB_CLOSE_DELAY=-1;DATABASE_TO_UPPER=FALSE"
        val ds = JdbcDataSource().apply { setURL(url) }
        val ctx = DSL.using(ds, SQLDialect.H2)
        ctx.execute(
            """
            CREATE TABLE IF NOT EXISTS organization (
                id   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                name VARCHAR(255) NOT NULL
            )
            """.trimIndent()
        )
        ctx.execute(
            """
            CREATE TABLE IF NOT EXISTS app_user (
                id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                name            VARCHAR(255) NOT NULL,
                email           VARCHAR(255) NOT NULL,
                organization_id BIGINT NOT NULL REFERENCES organization(id)
            )
            """.trimIndent()
        )
        ctx.execute(
            """
            CREATE TABLE IF NOT EXISTS category (
                id        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                name      VARCHAR(255) NOT NULL,
                parent_id BIGINT REFERENCES category(id)
            )
            """.trimIndent()
        )
        ctx.execute(
            """
            CREATE TABLE IF NOT EXISTS task (
                id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                title      VARCHAR(255) NOT NULL,
                created_by BIGINT NOT NULL REFERENCES app_user(id),
                updated_by BIGINT REFERENCES app_user(id)
            )
            """.trimIndent()
        )
        return ctx
    }

    private fun compile(extraSources: List<SourceFile> = emptyList()): KotlinCompilation.Result {
        val sources = generatedSources.map { (name, code) -> SourceFile.kotlin(name, code) } + extraSources
        return KotlinCompilation().apply {
            this.sources = sources
            inheritClassPath = true
            messageOutputStream = System.out
            // kotlin-compile-testing 1.6.0 bundles Kotlin 1.9.x compiler but our project uses Kotlin 2.x.
            // -Xskip-metadata-version-check allows the 1.9.x compiler to consume 2.x-compiled classes.
            kotlincArguments = listOf("-Xskip-metadata-version-check")
            // The generated code references dsl-runtime TEST classes (AppUserRecord, AppUserTable, etc.)
            // which live in the test classes dir, not in dsl-runtime.jar (main sources only).
            // Add the test classes dir explicitly so the compiler can resolve them.
            classpaths = classpaths + listOf(classDir)
        }.compile()
    }

